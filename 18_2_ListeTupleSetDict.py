# Liste
zahlen = [1, 2, 3, 4, 5]
print(zahlen)

zahlen.append(6)
print("Liste:", zahlen)

# Tuple
koordinaten = (10.0, 20.0)
print("Tuple:", koordinaten)

# Set
einzigartige_zahlen = {1, 2, 3, 4, 5}
einzigartige_zahlen.add(3)  # Duplikate werden ignoriert
print("Set:", einzigartige_zahlen)      

# Dictionary
person = {"Name": "Alice", "Alter": 30}
print("Dictionary:", person)    

