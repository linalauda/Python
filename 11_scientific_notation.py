# Positive Exponenten = große Zahlen
x = 1e3       # 1 × 10^3
print(x)      # 1000.0

y = 2.5e6     # 2.5 × 10^6
print(y)      # 2500000.0

# Negative Exponenten = kleine Zahlen
z = 3e-2      # 3 × 10^-2
print(z)      # 0.03

w = 4.7e-5    # 4.7 × 10^-5
print(w)      # 0.000047

scientific_float = 4.5e7
print(scientific_float) # 45000000.0


# Wissenschaftliche Notation im Rechnen
a = 1.2e3 + 3.4e2
print(a)      # 1540.0

b = 6.02e23 * 1e-3
print(b)      # 6.02e+20

# Auch mit float() möglich
c = float("2.5e-4")
print(c)      # 0.00025



